// Prints the numbers 0 to 5.
// (Same program as shown in lecture 1-1).
class PrintTo5 {
	public static void main(String[] args) {
		// הצהרת משתנים
        int i = 0;
        // ספירה עד 5 כולל והדפסה
        while (i < 6) {
            System.out.println(i);
            i = i + 1;
            
        }
        System.out.println("Done");
    }
 }